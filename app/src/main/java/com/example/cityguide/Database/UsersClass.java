package com.example.cityguide.Database;

import com.google.android.material.textfield.TextInputLayout;

public class UsersClass {
    String fullName, username, email,  password;

    public UsersClass(TextInputLayout fullName, TextInputLayout username, TextInputLayout email, TextInputLayout password) {
    }

    public UsersClass(String fullName, String username, String email, String password) {
        this.fullName = fullName;
        this.username = username;
        this.email = email;
        this.password = password;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
